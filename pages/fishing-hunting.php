<p><strong>Salmon & Steelhead:</strong> CLOSED</p>

<p><strong>Other Fish:</strong> Same as for rainbow trout locations and seasons</p>

<p>** Also See "Special Restrictions" section **</p>

<h3>Bag & Length limits</h3>

<p><strong>Lake Hiyúumptipin (Indian Lake) and All Open Streams:</strong>Five (5) trout per day, 8 inch minimum and only one trout over 20 inches in maximum length may be taken per day.  Two daily limits in possession must be accompanied with two days of day use/camping receipts. </p>
<p><strong>Other Fish:</strong> No bag or length limit.  Does not include Pacific Lamprey. </p> 

<h3>Special fishing restrictions</h3>

<p><strong>Lake Hiyúumptipin (Indian Lake):</strong> Only those boats powered by electric outboard motors or propelled by oars or other non-mechanized means may be operated. </p>

<p><strong>McKay Creek: </strong>Catch and release fly-fishing only with a single barbless hook is allowed January 1 to April 22, and November 1 to December 31, 2019.  Regular catch and keep regulations apply April 23 to October 31, 2019. </p>

<p><strong>Mainstem Umatilla River:</strong> Catch and release fly fishing only with a single barbless hook is allowed during the trout season on the mainstem Umatilla River and tributaries upstream from Thornhollow Bridge to the upper Reservation boundary.  Regular catch and keep regulations apply to mainstem and tributaries below Thornhollow Bridge, however, hooks no larger than ¼” (from point to shank) are allowed. </p>

<p><strong>At Fish Facilities:</strong>No person shall fish from or trespass on a fish way, weir, fish trap or fish facility water intake/outflow structure.  Bonifer Springs fishing closure includes the pond and two feeder springs.  Minthorn Springs fishing closure includes the fish facilities and Minthorn Springs Creek.  Imaques C-Mem-ini-kem, Thornhollow & Pendleton closures includes fish facilities and the water intake/outflow structures. </p>              

     
