<h2>Hunting Seasons</h2>
<h3>Special Hunting Restrictions</h3>
<p>Non-Indian hunting on the Umatilla Indian Reservation is restricted to upland game birds and waterfowl only. No big-game hunting is permitted.</p>

<p>No hunting of blue or ruffed grouse or dove, or spring Gobbler Turkey is permitted.</p>

<p>CTUIR sets bag and possession limits consistent with Oregon State Synopsis for these regulations.</p>

<p>Tribal lands harvest is not in addition to existing State limits.</p> 
  
<h3>Introduction</h3>
<ul style="list-style: none;">

<li>The following is a summary of regulations that apply to persons who are not members of the Confederated Tribes of the Umatilla Indian Reservation and who wish to fish within the boundaries of the Umatilla Indian Reservation: </li>

<li><strong>1</strong>. Any person who fishes or hunts on the lands or in the waters within the boundaries of the Umatilla Indian Reservation shall carry on his/her person at all times, a permit issued to them by the Confederated Tribes which specifically authorizes such activity. Possession of any fish or game, or parts thereof by persons who do not have a valid tribal permit shall be prima facie evidence that the fish or game was unlawfully taken; </li>

<li><strong>2</strong>. All persons who are not enrolled members of the Confederated Tribes who engage in fishing on reservation lands without a Tribal Permit are subject to federal criminal prosecution pursuant to 18 U.S.C. Section 1165; </li>

<li><strong>3</strong>. Tribal permits are not transferable to any other person.  Any attempt to transfer a Tribal Permit renders that permit null and void; and</li>

<li><strong>4</strong>. In addition to possessing a Tribal Permit, any person who fishes within the boundaries of the Umatilla Indian Reservation must comply with the State of Oregon general angling regulations regarding gear, bait, harvest methods, harvest hours and general restrictions. </li>
	
	
	
